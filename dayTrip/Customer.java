package dayTrip;

public class Customer {
	private String name;
	private int age;
	private String gender;
	private String nationality;
	private String email;
	
	public Customer (String name, int age, String gender, String nationality, String email ) {
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.nationality = nationality;
		this.email = email;
		
	}
	
}
